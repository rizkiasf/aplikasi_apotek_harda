    void koneksiDatabase() {
        try {
            Class.forName("com.mysql.cj.jdbc.Driver");
            conn = DriverManager.getConnection("jdbc:mysql://localhost/apotek_harda", "root", "");
            stmt = conn.createStatement();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Koneksi gagal: " + e.getMessage());
        }
    }

    void tampilData() {
        String[] kolom = {"Kode Obat", "Nama Obat", "Jenis Obat", "Jumlah Stok", "Harga Beli", "Harga Jual", "Tanggal Expired"};
        model = new DefaultTableModel(null, kolom);
        jTable1.setModel(model);

        try {
            String sql = "SELECT * FROM obat";
            rs = stmt.executeQuery(sql);
            while (rs.next()) {
                Object[] data = {
                    rs.getString("kode_obat"),
                    rs.getString("nama_obat"),
                    rs.getString("jenis_obat"),
                    rs.getInt("jumlah_stok"),
                    rs.getDouble("harga_beli"),
                    rs.getDouble("harga_jual"),
                    rs.getDate("tanggal_exp")
                };
                model.addRow(data);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Gagal tampil data: " + e.getMessage());
        }
    }
