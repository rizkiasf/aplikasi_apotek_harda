private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {
    int selectedRow = jTable1.getSelectedRow();

    if (selectedRow != -1) {
        // Ambil data dari baris yang diklik
        String kode = jTable1.getValueAt(selectedRow, 0).toString();
        String nama = jTable1.getValueAt(selectedRow, 1).toString();
        String jenis = jTable1.getValueAt(selectedRow, 2).toString();
        String stok = jTable1.getValueAt(selectedRow, 3).toString();
        String hargaBeli = jTable1.getValueAt(selectedRow, 4).toString();
        String hargaJual = jTable1.getValueAt(selectedRow, 5).toString();
        String exp = jTable1.getValueAt(selectedRow, 6).toString(); // Format yyyy-MM-dd

        // Set ke form input
        txtKode.setText(kode);
        txtNama.setText(nama);
        txtJenis.setText(jenis);
        txtStok.setText(stok);
        txtHargaBeli.setText(hargaBeli);
        txtHargaJual.setText(hargaJual);

        // Konversi tanggal dari String ke Date
        try {
            java.text.SimpleDateFormat sdf = new java.text.SimpleDateFormat("yyyy-MM-dd");
            java.util.Date tanggal = sdf.parse(exp);
            txtExp.setDate(tanggal);
        } catch (Exception e) {
            e.printStackTrace();
        }
    }
}

