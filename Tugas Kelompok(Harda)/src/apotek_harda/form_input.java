    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
    int selectedRow = jTable1.getSelectedRow();
    if (selectedRow != -1) {

        // Validasi input kosong
        if (txtStok.getText().isEmpty() || 
            txtHargaBeli.getText().isEmpty() || 
            txtHargaJual.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Stok, Harga Beli, dan Harga Jual tidak boleh kosong!");
            return;
        }

        // Validasi tanggal expired tidak boleh null
        if (txtExp.getDate() == null) {
            JOptionPane.showMessageDialog(this, "Tanggal Expired tidak boleh kosong!");
            return;
        }

        try {
            // Lanjut ke konversi dan query update
            String kodeObat = txtKode.getText();
            String namaObat = txtNama.getText();
            String jenisObat = txtJenis.getText();
            int stok = Integer.parseInt(txtStok.getText());
            double hargaBeli = Double.parseDouble(txtHargaBeli.getText());
            double hargaJual = Double.parseDouble(txtHargaJual.getText());
            java.util.Date utilDate = txtExp.getDate();
            java.sql.Date tanggalExp = new java.sql.Date(utilDate.getTime());

            String sql = "UPDATE obat SET nama_obat=?, jenis_obat=?, jumlah_stok=?, harga_beli=?, harga_jual=?, tanggal_exp=? WHERE kode_obat=?";
            PreparedStatement pst = conn.prepareStatement(sql);
            pst.setString(1, namaObat);
            pst.setString(2, jenisObat);
            pst.setInt(3, stok);
            pst.setDouble(4, hargaBeli);
            pst.setDouble(5, hargaJual);
            pst.setDate(6, tanggalExp);
            pst.setString(7, kodeObat);
            pst.executeUpdate();

            JOptionPane.showMessageDialog(this, "Data berhasil diubah!");
            tampilData();
            kosongkanForm();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Gagal edit: " + e.getMessage());
        }
    } else {
        JOptionPane.showMessageDialog(this, "Pilih data terlebih dahulu.");
    }
    }//GEN-LAST:event_jButton3ActionPerformed

